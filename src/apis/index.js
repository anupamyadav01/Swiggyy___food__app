const BASE_URL =
  "https://www.swiggy.com/dapi/restaurants/list/v5?lat=26.95250&lng=75.71050&is-seo-homepage-enabled=true&page_type=DESKTOP_WEB_LISTING";

const getFoodTypesData = async () => {
  try {
    const response = await fetch(BASE_URL);
    const results = await response.json();
    return results?.data;
  } catch (error) {
    throw new Error("Error while fetchig data", error);
  }
};

export { getFoodTypesData };
